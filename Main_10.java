public class Main_10 {
    public static void main(String[] args) {
        /* switch case */
        int i = 3;

        switch(i ){
            case 1:
                System. out.println("변수 i는 1 입니다." );
                break;
            case 2:
                System. out.println("변수 i는 2입니다." );
                break;
            case 3:
                System. out.println("변수 i는 3입니다." );
                break;
            default:
                System. out.println("변수 1,2,3이 아닌 값입니다." );
                break;
        }
    }
}